/**
 * CI Generator Command
 * Generates CI/CD pipeline configuration (GitHub Actions).
 */

const fs = require('fs');
const path = require('path');
const chalk = require('chalk');
const { TechStackDetector } = require('../../utils/tech-stack-detector');
const {
  resolveTestCommands,
  _detectPackageManager: detectPackageManager,
} = require('../../utils/test-command-resolver');
const { createLogger } = require('../../utils/logger');
const logger = createLogger('ci-generator');

const CI_FILE_NAME = 'chaos-ci.yml';

class CiGeneratorCommand {
  constructor(cwd) {
    this.cwd = cwd || process.cwd();
  }

  async execute(platform, options = {}) {
    const targetPlatform = platform || 'github';

    if (targetPlatform !== 'github') {
      throw new Error(`Unsupported platform: ${targetPlatform}. Currently only 'github' is supported.`);
    }

    const workflowsDir = path.join(this.cwd, '.github', 'workflows');
    const ciFilePath = path.join(workflowsDir, CI_FILE_NAME);

    if (fs.existsSync(ciFilePath) && !options.force) {
      throw new Error(
        `CI file already exists: .github/workflows/${CI_FILE_NAME}\n` +
        `Use --force to overwrite.`
      );
    }

    fs.mkdirSync(workflowsDir, { recursive: true });

    const techStack = TechStackDetector.analyze(this.cwd);
    const yamlContent = this._generateGithubActionsYaml(techStack);

    fs.writeFileSync(ciFilePath, yamlContent, 'utf-8');

    console.log(chalk.green(`\n✅ Generated .github/workflows/${CI_FILE_NAME}`));
    console.log(chalk.dim(`   Detected tech stack: ${techStack.language}${techStack.framework !== 'unknown' ? ` (${techStack.framework})` : ''}`));

    return {
      platform: targetPlatform,
      path: ciFilePath,
      techStack,
      force: options.force,
    };
  }

  _generateGithubActionsYaml(techStack) {
    const setupSteps = this._generateSetupSteps(techStack);
    const guardStep = this._generateGuardStep(techStack);
    const testSteps = this._generateTestSteps(techStack);

    return `# Chaos Code CI/CD Pipeline
# Auto-generated by "chaos ci-generator"
# https://github.com/Marcher-lam/chaos-code

name: Chaos Code CI/CD

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

jobs:
  chaos-pipeline:
    name: Chaos Code Quality Gate
    runs-on: ubuntu-latest

    steps:
      - name: Checkout
        uses: actions/checkout@v4

${setupSteps}
      - name: Install Dependencies
        run: ${this._getInstallCommand(techStack)}

      - name: Chaos Code Guard
        run: ${guardStep}

${testSteps}
`;
  }

  _generateSetupSteps(techStack) {
    if (techStack.language === 'node') {
      const packageManager = this._detectNodePackageManager();

      if (packageManager === 'pnpm') {
        return `      - name: Setup pnpm
        uses: pnpm/action-setup@v4
        with:
          version: 9

      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version-file: '.nvmrc'
          cache: 'pnpm'
`;
      }

      return `      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version-file: '.nvmrc'
          cache: '${packageManager}'
`;
    }

    if (techStack.language === 'python') {
      return `      - name: Setup Python
        uses: actions/setup-python@v5
        with:
          python-version: '3.11'
          cache: 'pip'
`;
    }

    if (techStack.language === 'rust') {
      return `      - name: Setup Rust
        uses: dtolnay/rust-toolchain@stable
`;
    }

    if (techStack.language === 'go') {
      return `      - name: Setup Go
        uses: actions/setup-go@v5
        with:
          go-version: '1.21'
`;
    }

    // Default: generic Node setup
    return `      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: '20'
          cache: 'npm'
`;
  }

  _getInstallCommand(techStack) {
    if (techStack.language === 'python') {
      return 'pip install -r requirements.txt';
    }
    if (techStack.language === 'rust') {
      return 'cargo build --all-targets';
    }
    if (techStack.language === 'go') {
      return 'go mod download';
    }
    if (techStack.language === 'node') {
      const packageManager = this._detectNodePackageManager();
      if (packageManager === 'pnpm') return 'pnpm install --frozen-lockfile';
      if (packageManager === 'yarn') return 'yarn install --frozen-lockfile';
      return 'npm ci';
    }
    return 'npm ci';
  }

  _generateGuardStep(techStack) {
    if (techStack.language === 'python') {
      return 'python -m stdd.guard --no-lint';
    }
    return 'npx chaos guard --no-lint';
  }

  _generateTestSteps(techStack) {
    if (techStack.language === 'node') {
      const commands = resolveTestCommands(this.cwd);
      if (commands.length > 0) {
        return commands.map(command => this._formatNodeTestStep(command)).join('\n');
      }
    }

    const testStep = this._generateTestStep(techStack);
    return `      - name: Run Tests
        run: ${testStep}`;
  }

  _formatNodeTestStep(command) {
    if (command.source === 'workspace') {
      const relativeCwd = path.relative(this.cwd, command.cwd).replace(/\\/g, '/');
      const label = this._escapeYamlDoubleQuoted(`${command.workspaceName} (${relativeCwd})`);
      return `      - name: "Run Tests - ${label}"
        run: cd "${relativeCwd}" && ${command.command}`;
    }

    const label = command.workspaceName && command.workspaceName !== 'root'
      ? ` - ${this._escapeYamlDoubleQuoted(command.workspaceName)}`
      : '';
    return `      - name: "Run Tests${label}"
        run: ${command.command}`;
  }

  _generateTestStep(techStack) {
    if (techStack.language === 'python') {
      if (techStack.testRunner === 'pytest') {
        return 'pytest';
      }
      return 'python -m unittest discover';
    }
    if (techStack.language === 'rust') {
      return 'cargo test';
    }
    if (techStack.language === 'go') {
      return 'go test ./...';
    }
    return 'npm test';
  }

  _detectNodePackageManager() {
    if (fs.existsSync(path.join(this.cwd, 'pnpm-workspace.yaml'))) return 'pnpm';

    const rootPkg = this._readRootPackageJson();
    const hasWorkspaces = this._hasPackageWorkspaces(rootPkg);
    if (hasWorkspaces && fs.existsSync(path.join(this.cwd, 'yarn.lock'))) return 'yarn';
    if (hasWorkspaces && fs.existsSync(path.join(this.cwd, 'package-lock.json'))) return 'npm';

    return detectPackageManager(this.cwd);
  }

  _readRootPackageJson() {
    try {
      return JSON.parse(fs.readFileSync(path.join(this.cwd, 'package.json'), 'utf-8'));
    } catch (err) {
      logger.warn(err.message);
      return null;
    }
  }

  _hasPackageWorkspaces(pkg) {
    return !!(pkg && (
      Array.isArray(pkg.workspaces) ||
      (pkg.workspaces && Array.isArray(pkg.workspaces.packages))
    ));
  }

  _escapeYamlDoubleQuoted(value) {
    return String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
  }
}

module.exports = { CiGeneratorCommand, CI_FILE_NAME };
